def print_art_logo():
    # 定义不同颜色的ANSI转义码（一行一种颜色）
    colors = [
        "\033[31m",  # 红色
        "\033[32m",  # 绿色
        "\033[33m",  # 黄色
        "\033[34m",  # 蓝色
        "\033[35m",  # 紫色
        "\033[36m",  # 青色
    ]
    # 重置颜色的转义码
    reset = "\033[0m"

    lines = [
        "██╗ ██╗  ██╗██╗   ██╗███╗   ██╗",
        "██║ ██║ ██╔╝██║   ██║████╗  ██║",
        "██║ █████╔╝ ██║   ██║██╔██╗ ██║",
        "██║ ██╔═██╗ ██║   ██║██║╚██╗██║",
        "██║ ██║  ██╗╚██████╔╝██║ ╚████║",
        "╚═╝ ╚═╝  ╚═╝ ╚═════╝ ╚═╝  ╚═══╝"
    ]

    # 逐行打印，每行使用不同颜色
    for i, line in enumerate(lines):
        color = colors[i % len(colors)]
        print(f"{color}{line}{reset}")

if __name__ == "__main__":
    print_art_logo()